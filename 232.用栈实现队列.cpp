// @lc code=start
class MyQueue {
    stack<int> stIn;
    stack<int> stOut;

public:
    MyQueue() {

    }
    
    void push(int x) {
        stIn.push(x);
    }
    
    int pop() {
        if (stOut.empty()){
            while (!stIn.empty()){
                stOut.push(stIn.top());
                stIn.pop();
            }
        }
        int result = stOut.top();
        stOut.pop();
        return result;
    }
    
    int peek() {
        int s = this -> pop();
        stOut.push(s);
        return s;
    }
    
    bool empty() {
        return stIn.empty() && stOut.empty();
    }
};

